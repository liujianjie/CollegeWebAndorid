package com.jlgouwu.pojo;

public class JRuser {
	private int jruser_id;
	private String uname;
	private String pwd;
	private String jr_img;
	private String jointime;
	private String birthday;
	private float moneys;
	private String phone;
	public int getJruser_id() {
		return jruser_id;
	}
	public void setJruser_id(int jruser_id) {
		this.jruser_id = jruser_id;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getJointime() {
		return jointime;
	}
	public void setJointime(String jointime) {
		this.jointime = jointime;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public float getMoneys() {
		return moneys;
	}
	public void setMoneys(float moneys) {
		this.moneys = moneys;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getJr_img() {
		return jr_img;
	}
	public void setJr_img(String jr_img) {
		this.jr_img = jr_img;
	}
}
