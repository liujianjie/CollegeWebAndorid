public class 关系运算
{
	public static void main(String sad[]){
	//定义变量
	int a=21,b=4;
	//运算 算是熟悉输出吧
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.print(a*b);
	System.out.println("\t"+(a*b));//如果需要占位符 且 要相乘就这样写
	System.out.print(a/b);
	System.out.println("\t"+(a/b));
	System.out.println(a%b);
	System.out.println(10%4);
	System.out.println(10%-4);
	System.out.println(-10%4);
	System.out.println(-10%4);
	System.out.println(++a);
	System.out.println(a++);
	System.out.println(--a);
	System.out.println(a--);
	System.out.println(""+a+b);
	}
}